def generate_synthetic_dataset(
    model: S3DModel,
    n_samples: int = 5000,
    n_keypoints: int = 11,
    activation_low: float = 40.0,
    activation_high: float = 100.0,
    max_iter: int = 100,
    seed: int | None = 0,
    batch_size: int = 256,
    use_torch: bool = True,
    rest_positions: np.ndarray | None = None,
    midline_atoms: np.ndarray | None = None,
    verbose: bool = False,
) -> tuple[np.ndarray, np.ndarray]:
    """Run the forward solver n_samples times on random activations.

    For each sample: draw activations in [activation_low, activation_high],
    solve forward, extract midline atoms, resample to ``n_keypoints``,
    compute displacement from rest pose, flatten to 22-dim.

    Args:
        model: parsed S3D model.
        n_samples: number of samples to generate.
        n_keypoints: resampled midline length (default 11).
        activation_low: lower bound on activation percentages.
        activation_high: upper bound on activation percentages.
        max_iter: solver iterations.
        seed: RNG seed (None -> random).
        batch_size: torch batch size (ignored if use_torch=False).
        use_torch: use batched torch solver (default True).
        rest_positions: optional precomputed rest equilibrium positions. If
            None, solved from activations=100.
        midline_atoms: optional precomputed midline atom indices. If None,
            computed from the rest positions.
        verbose: print progress.

    Returns:
        X: (n_samples, n_keypoints * 2) displacement vectors.
        Y: (n_samples, model.n_muscles) activations.
    """
    rng = np.random.default_rng(seed)
    K = model.n_muscles

    # Rest pose: solve at 100% everywhere.
    if rest_positions is None:
        rest_positions = solve_equilibrium(
            positions=model.positions,
            strut_pairs=model.strut_pairs,
            rest_lengths=model.rest_lengths,
            elasticity_r=model.elasticity_r,
            elasticity_c=model.elasticity_c,
            strut_muscles=model.strut_muscles,
            fixing=model.fixing,
            muscle_pcts=np.full(K, 100.0),
            max_iter=max_iter,
            symmetry_axes=model.symmetry_axes,
            symmetry_coord=model.symmetry_coord,
        )

    if midline_atoms is None:
        midline_atoms = midline_indices(rest_positions, model.fixing, model.polygons)

    # Determine frozen axis from rest midline (should match the symmetry
    # plane normal).
    pts_rest = rest_positions[midline_atoms]
    frozen_axis = int(np.argmin(pts_rest.var(axis=0)))
    keep_axes = [a for a in range(3) if a != frozen_axis]

    rest_keypoints = resample_curve(pts_rest[:, keep_axes], n_keypoints)

    # Sample all activations up front.
    Y = rng.uniform(activation_low, activation_high, size=(n_samples, K))

    X = np.empty((n_samples, n_keypoints * 2), dtype=np.float64)

    if use_torch and _TORCH_AVAILABLE:
        t0 = time.perf_counter()
        for start in range(0, n_samples, batch_size):
            end = min(start + batch_size, n_samples)
            acts_batch = Y[start:end]
            pos_batch = batched_forward_solve(model, acts_batch, max_iter=max_iter)
            for i in range(end - start):
                pts = pos_batch[i, midline_atoms][:, keep_axes]
                kps = resample_curve(pts, n_keypoints)
                X[start + i] = (kps - rest_keypoints).reshape(-1)
            if verbose:
                elapsed = time.perf_counter() - t0
                print(f"  [{end}/{n_samples}] {elapsed:.2f}s")
    else:
        t0 = time.perf_counter()
        for i in range(n_samples):
            pos = solve_equilibrium(
                positions=model.positions,
                strut_pairs=model.strut_pairs,
                rest_lengths=model.rest_lengths,
                elasticity_r=model.elasticity_r,
                elasticity_c=model.elasticity_c,
                strut_muscles=model.strut_muscles,
                fixing=model.fixing,
                muscle_pcts=Y[i],
                max_iter=max_iter,
                symmetry_axes=model.symmetry_axes,
                symmetry_coord=model.symmetry_coord,
            )
            pts = pos[midline_atoms][:, keep_axes]
            kps = resample_curve(pts, n_keypoints)
            X[i] = (kps - rest_keypoints).reshape(-1)
            if verbose and (i + 1) % 500 == 0:
                elapsed = time.perf_counter() - t0
                print(f"  [{i + 1}/{n_samples}] {elapsed:.2f}s")

    return X, Y


# ---------------------------------------------------------------------------
# Ridge regression (pure NumPy)
# ---------------------------------------------------------------------------


def fit_ridge(
    X: np.ndarray, Y: np.ndarray, alpha: float = 1.0
) -> tuple[np.ndarray, np.ndarray]:
    """Closed-form ridge regression with unregularised bias.

    Fits ``Y ~ X W + b`` by augmenting X with a bias column, regularising
    only the weight block (not the bias).

    Args:
        X: (n, d) input features.
        Y: (n, k) targets.
        alpha: ridge strength (default 1.0).

    Returns:
        W: (d, k) weight matrix.
        b: (k,) bias vector.
    """
    X = np.asarray(X, dtype=np.float64)
    Y = np.asarray(Y, dtype=np.float64)
    n, d = X.shape
    if Y.shape[0] != n:
        raise ValueError(f"X n={n} != Y n={Y.shape[0]}")

    # Append bias column of ones.
    X_aug = np.concatenate([X, np.ones((n, 1), dtype=np.float64)], axis=1)  # (n, d+1)
    # Regularisation matrix: alpha * I but with 0 on the bias diagonal.
    reg = alpha * np.eye(d + 1, dtype=np.float64)
    reg[-1, -1] = 0.0

    A = X_aug.T @ X_aug + reg  # (d+1, d+1)
    B = X_aug.T @ Y  # (d+1, k)
    coeffs = np.linalg.solve(A, B)  # (d+1, k)
    W = coeffs[:-1]  # (d, k)
    b = coeffs[-1]  # (k,)
    return W, b


def predict_activations(
    keypoint_displacements: np.ndarray, W: np.ndarray, b: np.ndarray
) -> np.ndarray:
    """Predict clipped activation vector from displacement features.

    Args:
        keypoint_displacements: (d,) or (n, d) displacement features.
        W: (d, k) weight matrix.
        b: (k,) bias.

    Returns:
        Activations clipped to [0, 100].
    """
    out = keypoint_displacements @ W + b
    np.clip(out, 0.0, 100.0, out=out)
    return out


# ---------------------------------------------------------------------------
# Persistable data structure
# ---------------------------------------------------------------------------


@dataclass(frozen=True)
class InverseMapping:
    """Trained inverse keypoint -> activation mapping."""

    W: np.ndarray  # (n_keypoints * 2, n_muscles)
    b: np.ndarray  # (n_muscles,)
    rest_keypoints: np.ndarray  # (n_keypoints, 2)
    midline_indices: np.ndarray  # atom indices on the midline
    frozen_axis: int  # which 3D axis is the symmetry-plane normal
    muscle_names: list[str]
    n_keypoints: int
    alpha: float


def save_inverse_mapping(mapping: InverseMapping, path: str | Path) -> None:
    """Serialise an InverseMapping to a .npz archive."""
    path = Path(path)
    np.savez(
        path,
        W=mapping.W,
        b=mapping.b,
        rest_keypoints=mapping.rest_keypoints,
        midline_indices=mapping.midline_indices,
        frozen_axis=np.int64(mapping.frozen_axis),
        muscle_names=np.array(mapping.muscle_names, dtype=object),
        n_keypoints=np.int64(mapping.n_keypoints),
        alpha=np.float64(mapping.alpha),
    )


def load_inverse_mapping(path: str | Path) -> InverseMapping:
    """Load an InverseMapping from a .npz archive."""
    path = Path(path)
    with np.load(path, allow_pickle=True) as data:
        return InverseMapping(
            W=data["W"],
            b=data["b"],
            rest_keypoints=data["rest_keypoints"],
            midline_indices=data["midline_indices"],
            frozen_axis=int(data["frozen_axis"]),
            muscle_names=list(data["muscle_names"]),
            n_keypoints=int(data["n_keypoints"]),
            alpha=float(data["alpha"]),
        )


# ---------------------------------------------------------------------------
# Convenience: end-to-end build
# ---------------------------------------------------------------------------


def build_inverse_mapping(
    model: S3DModel,
    n_samples: int = 5000,
    n_keypoints: int = 11,
    alpha: float = 1.0,
    activation_low: float = 40.0,
    activation_high: float = 100.0,
    max_iter: int = 100,
    seed: int | None = 0,
    use_torch: bool = True,
    batch_size: int = 256,
    verbose: bool = False,
) -> InverseMapping:
    """Train an InverseMapping end-to-end from an S3D model.

    Solves rest pose, extracts midline atoms, generates synthetic data,
    fits ridge regression.
    """
    rest_positions = solve_equilibrium(
        positions=model.positions,
        strut_pairs=model.strut_pairs,
        rest_lengths=model.rest_lengths,
        elasticity_r=model.elasticity_r,
        elasticity_c=model.elasticity_c,
        strut_muscles=model.strut_muscles,
        fixing=model.fixing,
        muscle_pcts=np.full(model.n_muscles, 100.0),
        max_iter=max_iter,
        symmetry_axes=model.symmetry_axes,
        symmetry_coord=model.symmetry_coord,
    )
    midline = midline_indices(rest_positions, model.fixing, model.polygons)
    pts_rest = rest_positions[midline]
    frozen_axis = int(np.argmin(pts_rest.var(axis=0)))
    keep_axes = [a for a in range(3) if a != frozen_axis]
    rest_keypoints = resample_curve(pts_rest[:, keep_axes], n_keypoints)

    X, Y = generate_synthetic_dataset(
        model=model,
        n_samples=n_samples,
        n_keypoints=n_keypoints,
        activation_low=activation_low,
        activation_high=activation_high,
        max_iter=max_iter,
        seed=seed,
        batch_size=batch_size,
        use_torch=use_torch,
        rest_positions=rest_positions,
        midline_atoms=midline,
        verbose=verbose,
    )
    W, b = fit_ridge(X, Y, alpha=alpha)

    return InverseMapping(
        W=W,
        b=b,
        rest_keypoints=rest_keypoints,
        midline_indices=midline,
        frozen_axis=frozen_axis,
        muscle_names=list(model.muscle_names),
        n_keypoints=n_keypoints,
        alpha=alpha,
    )
