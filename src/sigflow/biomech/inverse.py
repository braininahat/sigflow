"""Inverse mapping: tongue keypoints -> muscle activations.

Given DLC dorsal-chain keypoints observed in ultrasound, estimate the 23-dim
muscle activation percentages that produced that tongue shape. Pure NumPy
ridge regression trained on a synthetic dataset generated by the forward
biomechanical solver.

Optimisation choice
-------------------
Option A: batched PyTorch forward solver.

The naive approach of calling the NumPy ``solve_equilibrium`` 5000 times is
~320 s on this machine (65 ms/call at 100 iters). We port the Jacobi
relaxation in ``solver.py`` to PyTorch with a leading batch dimension ``B``
so all samples are solved in parallel on the GPU (CPU fallback). The main
bottleneck in NumPy is the Python loop; vectorising across the batch is a
dense matmul/scatter problem that fits GPU workloads well.

We picked Option A over Option B (neural surrogate) because:
* A is exact — the batched solver is numerically equivalent to the NumPy
  solver (parity test with ``atol=1e-4`` before dataset generation).
* No approximation error to characterise.
* Target 5000 samples in <2 s on GPU / <10 s on CPU — comfortably met.

The module still uses the original NumPy ``solve_equilibrium`` as the
source-of-truth for parity tests and as a CPU fallback if Torch is missing.

See ``docs/plans/biomech-inverse-mapping.md`` for the full design.
"""

