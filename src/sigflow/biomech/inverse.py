# Batched PyTorch forward solver
# ---------------------------------------------------------------------------


def batched_forward_solve(
    model: S3DModel,
    activations: "np.ndarray | torch.Tensor",
    max_iter: int = 100,
    device: "str | None" = None,
) -> np.ndarray:
    """Batched Jacobi relaxation forward solver.

    Numerically equivalent to running ``solve_equilibrium`` B times. All
    tensors gain a leading batch dimension. Uses CUDA if available.

    Args:
        model: parsed S3D model.
        activations: (B, K) muscle activation percentages.
        max_iter: relaxation iterations (default 100, same as NumPy solver).
        device: torch device name. Defaults to 'cuda' if available, else 'cpu'.

    Returns:
        (B, N, 3) equilibrium positions as float64 numpy array.
    """
    if not _TORCH_AVAILABLE:
        raise RuntimeError("PyTorch is required for batched_forward_solve.")

    if device is None:
        device = "cuda" if torch.cuda.is_available() else "cpu"
    dev = torch.device(device)
    dtype = torch.float64

    acts = torch.as_tensor(np.asarray(activations, dtype=np.float64), dtype=dtype, device=dev)
    if acts.ndim != 2:
        raise ValueError(f"activations must be (B, K), got shape {tuple(acts.shape)}.")
    B, K = acts.shape
    if K != model.n_muscles:
        raise ValueError(
            f"activations K={K} != model.n_muscles={model.n_muscles}"
        )

    n_atoms = model.n_atoms
    # Rest positions broadcast to (B, N, 3)
    base_pos = torch.as_tensor(model.positions, dtype=dtype, device=dev)
    pos = base_pos.unsqueeze(0).expand(B, -1, -1).contiguous()

    # Strut connectivity (shared across batch)
    strut_pairs = torch.as_tensor(model.strut_pairs, dtype=torch.long, device=dev)
    a1 = strut_pairs[:, 0]
    a2 = strut_pairs[:, 1]

    rest = torch.as_tensor(model.rest_lengths, dtype=dtype, device=dev)
    er = torch.as_tensor(model.elasticity_r, dtype=dtype, device=dev)
    ec = torch.as_tensor(model.elasticity_c, dtype=dtype, device=dev)
    strut_muscles = torch.as_tensor(model.strut_muscles, dtype=torch.long, device=dev)
    fixing = torch.as_tensor(model.fixing, dtype=torch.int32, device=dev)

    M = strut_pairs.shape[0]
    has_muscle = strut_muscles >= 0
    musc_idx = strut_muscles.clamp(min=0)  # safe indices (masked later)

    # Per-batch target lengths and elasticities over struts.
    # acts[:, musc_idx] -> (B, M), same broadcasting rule as NumPy advanced indexing
    muscle_pct = acts[:, musc_idx]  # (B, M)
    # target_lengths[i, j] = max(rest[j] * pct/100, 1.0) if strut has muscle
    scaled = rest.unsqueeze(0) * (muscle_pct / 100.0)
    scaled = torch.clamp(scaled, min=1.0)
    rest_b = rest.unsqueeze(0).expand(B, -1)
    target = torch.where(has_muscle.unsqueeze(0), scaled, rest_b)

    ratio = target / rest_b
    elast_active = (er.unsqueeze(0) - ec.unsqueeze(0)) * ratio + ec.unsqueeze(0)
    er_b = er.unsqueeze(0).expand(B, -1)
    elasticity = torch.where(has_muscle.unsqueeze(0), elast_active, er_b)  # (B, M)

    free_mask = (fixing == 0).unsqueeze(0)  # (1, N)
    sym_mask = (fixing == 2).unsqueeze(0)  # (1, N)

    # Pre-allocate scratch buffers.
    forces = torch.empty((B, n_atoms, 3), dtype=dtype, device=dev)
    weights = torch.empty((B, n_atoms), dtype=dtype, device=dev)

    # Axis to zero out when updating symmetric atoms: keep the two axes that
    # span the symmetry plane free, zero the normal.
    if model.symmetry_axes == 0:  # XY plane: freeze Z
        freeze_axis = 2
    elif model.symmetry_axes == 1:  # XZ plane: freeze Y
        freeze_axis = 1
    else:  # YZ plane: freeze X
        freeze_axis = 0

    # index_add needs LongTensor indices
    a1_b = a1.unsqueeze(0).expand(B, -1)
    a2_b = a2.unsqueeze(0).expand(B, -1)

    for _ in range(max_iter):
        p1 = pos.gather(1, a1_b.unsqueeze(-1).expand(-1, -1, 3))  # (B, M, 3)
        p2 = pos.gather(1, a2_b.unsqueeze(-1).expand(-1, -1, 3))
        diff = p1 - p2  # (B, M, 3)
        dist = torch.linalg.norm(diff, dim=2)  # (B, M)
        dist = torch.clamp(dist, min=1e-10)

        b = (dist - target) / dist / 2.0  # (B, M)
        active = b > 0
        b_e = torch.where(active, b * elasticity, torch.zeros_like(b))  # (B, M)
        w_e = torch.where(active, elasticity, torch.zeros_like(elasticity))  # (B, M)

        force_vec = diff * b_e.unsqueeze(-1)  # (B, M, 3)

        forces.zero_()
        weights.zero_()
        # Scatter forces: atom a1 -= force_vec, a2 += force_vec.
        forces.scatter_add_(1, a1_b.unsqueeze(-1).expand(-1, -1, 3), -force_vec)
        forces.scatter_add_(1, a2_b.unsqueeze(-1).expand(-1, -1, 3), force_vec)
        weights.scatter_add_(1, a1_b, w_e)
        weights.scatter_add_(1, a2_b, w_e)

        nonzero = weights > 0
        safe_w = torch.where(nonzero, weights, torch.ones_like(weights))
        forces = forces / safe_w.unsqueeze(-1)
        forces = torch.where(nonzero.unsqueeze(-1), forces, torch.zeros_like(forces))

        # Free atoms: full update.
        pos = torch.where(free_mask.unsqueeze(-1), pos + forces, pos)

        # Symmetric atoms: zero the frozen axis in the update.
        sym_force = forces.clone()
        sym_force[..., freeze_axis] = 0.0
        pos = torch.where(sym_mask.unsqueeze(-1), pos + sym_force, pos)

    return pos.detach().cpu().numpy()


def _parity_check_batched_vs_numpy(
    model: S3DModel,
    n_samples: int = 4,
    atol: float = 1e-4,
    rng: np.random.Generator | None = None,
) -> tuple[bool, float]:
    """Run the batched torch solver and the NumPy solver on the same
    activations and compare. Returns (passed, max_abs_diff)."""
    if rng is None:
        rng = np.random.default_rng(0)
    acts = rng.uniform(40.0, 100.0, size=(n_samples, model.n_muscles))

    torch_out = batched_forward_solve(model, acts, max_iter=100)

    np_out = np.empty_like(torch_out)
    for i in range(n_samples):
        np_out[i] = solve_equilibrium(
            positions=model.positions,
            strut_pairs=model.strut_pairs,
            rest_lengths=model.rest_lengths,
            elasticity_r=model.elasticity_r,
            elasticity_c=model.elasticity_c,
            strut_muscles=model.strut_muscles,
            fixing=model.fixing,
            muscle_pcts=acts[i],
            max_iter=100,
            symmetry_axes=model.symmetry_axes,
            symmetry_coord=model.symmetry_coord,
        )

    diff = np.abs(torch_out - np_out)
    return bool(diff.max() <= atol), float(diff.max())


