# ---------------------------------------------------------------------------
# Midline extraction and resampling
# ---------------------------------------------------------------------------


def midline_indices(
    positions: np.ndarray,
    fixing: np.ndarray,
    polygons: list[np.ndarray],
) -> np.ndarray:
    """Ordered atom indices tracing the midsagittal surface curve.

    The .s3d half-model is closed off by a cap of triangles whose vertices
    all lie on the symmetry plane. The outline of that cap is a clean 1D
    closed loop — exactly what we want for the midsagittal curve. We

    1. collect all polygons whose vertices are all on the symmetry plane
       (``fixing == 2``);
    2. count edge multiplicities inside that sub-mesh — boundary edges
       appear exactly once (interior edges appear twice);
    3. build an adjacency list from the boundary edges;
    4. walk the cycle from its lowest-variance-axis extreme point.

    If the polygon cap walk cannot be formed (defensive fallback), drop
    back to a greedy nearest-neighbour walk across all symmetric surface
    atoms.

    Args:
        positions: (N, 3) atom positions.
        fixing: (N,) fixing flags (0=free, 1=static, 2=symmetric).
        polygons: list of polygon faces (each an int array of atom indices).

    Returns:
        (K,) int64 array of atom indices, ordered along the curve.
    """
    sym_atoms = np.where(fixing == 2)[0]
    if sym_atoms.size == 0:
        raise ValueError("No symmetric atoms (fixing == 2) in the model.")
    sym_set = set(int(i) for i in sym_atoms)

    # Count edges that lie entirely inside the symmetric-only polygon cap.
    edge_count: dict[tuple[int, int], int] = {}
    for face in polygons:
        verts = [int(v) for v in face]
        if not verts or not all(v in sym_set for v in verts):
            continue
        n = len(verts)
        for i in range(n):
            u, v = verts[i], verts[(i + 1) % n]
            e = (u, v) if u < v else (v, u)
            edge_count[e] = edge_count.get(e, 0) + 1

    boundary = [e for e, c in edge_count.items() if c == 1]

    if boundary:
        adj: dict[int, list[int]] = {}
        for u, v in boundary:
            adj.setdefault(u, []).append(v)
            adj.setdefault(v, []).append(u)

        # All degrees should be 2 on a clean cycle. If not, fall back below.
        if all(len(n) == 2 for n in adj.values()):
            # Project onto 2D for start-point selection.
            cand = np.array(sorted(adj.keys()), dtype=np.int64)
            pts = positions[cand]
            frozen_axis = int(np.argmin(pts.var(axis=0)))
            keep = [a for a in range(3) if a != frozen_axis]
            pts2d = pts[:, keep]
            start_axis = int(np.argmax(pts2d.var(axis=0)))
            start = int(cand[np.argmin(pts2d[:, start_axis])])

            order_list: list[int] = [start]
            visited = {start}
            prev: int | None = None
            curr = start
            while True:
                nexts = [x for x in adj[curr] if x != prev]
                if not nexts:
                    break
                nxt = nexts[0]
                if nxt == start:
                    break
                if nxt in visited:
                    break
                order_list.append(nxt)
                visited.add(nxt)
                prev = curr
                curr = nxt
            return np.asarray(order_list, dtype=np.int64)

    # Fallback: greedy NN walk over all sym atoms that appear in any polygon.
    in_polygon = np.zeros(len(positions), dtype=bool)
    for face in polygons:
        if len(face) == 0:
            continue
        in_polygon[np.asarray(face, dtype=np.int64)] = True
    candidate_mask = (fixing == 2) & in_polygon
    candidates = np.where(candidate_mask)[0]
    if candidates.size == 0:
        raise ValueError("No midline atoms found (fixing == 2 & in polygon).")

    pts = positions[candidates]
    variances = pts.var(axis=0)
    frozen_axis = int(np.argmin(variances))
    keep = [a for a in range(3) if a != frozen_axis]
    pts2d = pts[:, keep]

    axis_var = pts2d.var(axis=0)
    start_axis = int(np.argmax(axis_var))
    start_local = int(np.argmin(pts2d[:, start_axis]))

    n = len(candidates)
    visited_arr = np.zeros(n, dtype=bool)
    order = np.empty(n, dtype=np.int64)
    visited_arr[start_local] = True
    order[0] = start_local
    current = start_local
    for step in range(1, n):
        diff = pts2d - pts2d[current]
        d2 = np.einsum("ij,ij->i", diff, diff)
        d2[visited_arr] = np.inf
        nxt = int(np.argmin(d2))
        order[step] = nxt
        visited_arr[nxt] = True
        current = nxt

    return candidates[order]


def resample_curve(positions: np.ndarray, n_out: int = 11) -> np.ndarray:
    """Arc-length resample a 2D polyline to ``n_out`` equidistant samples.

    Args:
        positions: (K, 2) ordered points along a curve.
        n_out: number of output samples (default 11).

    Returns:
        (n_out, 2) resampled points.
    """
    if positions.ndim != 2 or positions.shape[1] != 2:
        raise ValueError(f"Expected (K, 2) array, got {positions.shape}.")
    if len(positions) < 2:
        raise ValueError("Need at least 2 points to resample a curve.")

    seg = np.linalg.norm(np.diff(positions, axis=0), axis=1)
    arc = np.concatenate([[0.0], np.cumsum(seg)])
    total = arc[-1]
    if total <= 0:
        raise ValueError("Degenerate curve (zero total length).")
    u = arc / total  # [0, 1]

    # scipy interp1d on each coordinate (kind='linear' for stability).
    t = np.linspace(0.0, 1.0, n_out)
    fx = interp1d(u, positions[:, 0], kind="linear", assume_sorted=True)
    fy = interp1d(u, positions[:, 1], kind="linear", assume_sorted=True)
    return np.stack([fx(t), fy(t)], axis=1)


